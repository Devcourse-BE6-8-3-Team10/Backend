package com.back.global.rsData

enum class ResultCode(
    val code: String,
    val status: Int,
    val defaultMessage: String
) {
    // ----------------------- [성공] -----------------------
    SUCCESS("200", 200, "요청이 성공적으로 처리되었습니다."),

    // ----------------------- [클라이언트 오류] -----------------------
    BAD_REQUEST("400", 400, "잘못된 요청입니다."),
    VALIDATION_ERROR("400", 400, "입력값 검증에 실패했습니다."),

    // ----------------------- [인증/인가 오류] -----------------------
    UNAUTHORIZED("401", 401, "인증이 필요합니다."),
    INVALID_CREDENTIALS("401", 401, "인증 정보가 올바르지 않습니다."),
    TOKEN_EXPIRED("401", 401, "토큰이 만료되었습니다."),
    FORBIDDEN("403", 403, "접근 권한이 없습니다."),

    // ----------------------- [리소스 없음] -----------------------
    NOT_FOUND("404", 404, "요청한 리소스를 찾을 수 없습니다."),
    MEMBER_NOT_FOUND("404", 404, "회원을 찾을 수 없습니다."),
    POST_NOT_FOUND("404", 404, "게시물을 찾을 수 없습니다."),

    // ----------------------- [서버 오류] -----------------------
    INTERNAL_ERROR("500", 500, "서버 내부 오류가 발생했습니다."),
    FILE_PROCESSING_ERROR("500", 500, "파일 처리 중 오류가 발생했습니다.")
}